'''
Swapping values of two variables using a third one
'''
print("Enter two variables")
variable1=input()
variable2=input()
print("Before swapping")
print("Variable1=",variable1)
print("Variable2=",variable2)
variable3=variable1
variable1=variable2
variable2=variable3
print("After swapping")
print("Variable1=",variable1)
print("Variable2=",variable2)